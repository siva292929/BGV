// ─── BULK CANDIDATE IMPORT ──────────────────────────────────────────────────

const multer = require('multer');
const csv = require('csv-parser');
const fs = require('fs');
const os = require('os');
const path = require('path');

const csvUpload = multer({ dest: os.tmpdir() });

router.post('/bulk-import', csvUpload.single('file'), async (req, res) => {
  try {
    const { hrId } = req.body;
    if (!req.file) return res.status(400).json({ error: "No file uploaded" });

    const results = [];
    const candidates = [];
    const errors = [];

    fs.createReadStream(req.file.path)
      .pipe(csv())
      .on('data', (data) => results.push(data))
      .on('end', async () => {
        // Clean up temp file
        fs.unlinkSync(req.file.path);

        for (const row of results) {
          try {
            const name = row.name || row.Name || row.NAME;
            const email = (row.email || row.Email || row.EMAIL)?.toLowerCase();

            if (!name || !email) {
              errors.push({ row, error: "Missing name or email" });
              continue;
            }

            const existingUser = await User.findOne({ email });
            if (existingUser) {
              errors.push({ row, error: "Email already exists" });
              continue;
            }

            const bestAgent = await User.findOne({ role: ROLES.AGENT }).sort({ taskCount: 1 });
            const tempPassword = crypto.randomBytes(4).toString('hex');
            const hashedPassword = await bcrypt.hash(tempPassword, 10);

            const newCandidate = new User({
              name,
              email,
              password: hashedPassword,
              role: ROLES.CANDIDATE,
              assignedAgent: bestAgent ? bestAgent.uid : null,
              createdBy: hrId || null,
              isFirstLogin: true
            });

            await newCandidate.save();

            // Extract all reference data fields from CSV row
            const hrData = {
              aadharNumber: row.aadharNumber || row.Aadhaar || '',
              panNumber: row.panNumber || row.PAN || '',
              tenthPercentage: row.tenthPercentage || row.Tenth || '',
              twelfthPercentage: row.twelfthPercentage || row.Twelfth || '',
              degreeGPA: row.degreeGPA || row.GPA || '',
              degreeName: row.degreeName || row.Degree || '',
              degreeUniversity: row.degreeUniversity || row.University || '',
              previousCompany: row.previousCompany || row.Company || '',
              previousDesignation: row.previousDesignation || row.Designation || '',
              previousDuration: row.previousDuration || row.Duration || '',
              hrContactName: row.hrContactName || '',
              hrContactEmail: row.hrContactEmail || '',
              hrContactPhone: row.hrContactPhone || '',
              ctc: row.ctc || row.CTC || '',
              remarks: row.remarks || row.Remarks || ''
            };

            const newBGVRequest = new BGVRequest({
              candidate: newCandidate.uid,
              hrData
            });
            await newBGVRequest.save();

            newCandidate.bgvRequest = newBGVRequest.uid;
            await newCandidate.save();

            if (bestAgent) {
              bestAgent.taskCount += 1;
              await bestAgent.save();
            }

            await emailService.sendInvitationEmail(name, email, tempPassword);
            candidates.push({ name, email });
          } catch (err) {
            errors.push({ row, error: err.message });
          }
        }

        res.json({
          message: `Processed ${results.length} rows`,
          importedCount: candidates.length,
          errorCount: errors.length,
          errors
        });
      });
  } catch (err) {
    if (req.file) fs.unlinkSync(req.file.path);
    res.status(500).json({ error: err.message });
  }
});

